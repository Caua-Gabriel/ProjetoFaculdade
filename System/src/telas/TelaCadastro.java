    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conectado = DriverManager.getConnection("jdbc:mysql://localhost:3306/projeto", "root", "1910");
            PreparedStatement st = conectado.prepareStatement("UPDATE cadastro SET senha = ?, cpf = ? WHERE email = ?");
            
            st.setString(1, txtEmail.getText());
            st.setString(2, txtSenha.getText());
            st.setString(3, txtCpf.getText());

            

            st.executeUpdate();
            JOptionPane.showMessageDialog(null, "Usuário Alterado com sucesso");
            
            btnAlterar.setVisible(false);
            txtEmail.setText("");
            txtSenha.setText("");
            txtSenha.setText("");

            
           
        } catch (ClassNotFoundException x) {
            JOptionPane.showMessageDialog(null, "Driver JDBC não encontrado " + x.getMessage());
        } catch (SQLException x) {
            JOptionPane.showMessageDialog(null, x.getMessage());
        } 

    }//GEN-LAST:event_btnAlterarActionPerformed

