    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection connector = DriverManager.getConnection("jdbc:mysql://localhost:3306/projeto", "root", "1910");

            String sql = ("INSERT INTO cadastro VALUES (?,?,?)");
            PreparedStatement st = connector.prepareStatement(sql);

            st.setString(1, txtEmail.getText());
            st.setString(2, txtSenha.getText());
            st.setString(3, txtCpf.getText());

            st.executeUpdate();

            JOptionPane.showMessageDialog(null, "Usuário cadatrado com sucesso");

        } catch (ClassNotFoundException x) {
            JOptionPane.showMessageDialog(null, "Driver.class JDBC não encontrada" + x.getMessage());
        } catch (SQLException x) {
            if (x.getMessage().contains("Duplicate entry")) {
                JOptionPane.showMessageDialog(null, "Este E-mail " + txtEmail.getText() + " já está cadastrado");
            } else {
                JOptionPane.showMessageDialog(null, "Erro na conexão com o banco de dados " + x.getMessage());
            }
            txtEmail.setText("");
            txtSenha.setText("");
            txtCpf.setText("");

            txtEmail.requestFocus();
        }

    }//GEN-LAST:event_btnCadastrarActionPerformed

